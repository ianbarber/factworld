"""Dense-vs-sparse per-step state supervision sweep (s5 / non-abelian composite).

This is the reproduction of the Phase-2 dense-supervision result on the current
benchmark, plus a sparsity sweep (K = how often the per-step holder is supervised)
and an inference-time (test-time-compute) probe.

Mechanism (port of ``phases/02-non-abelian-state/dense_capstone.py`` to the natural
format + a slot-friendly eval): train on streams that interleave the oracle's holder
of the queried role after every K events. K=1 is dense; large K is answer-only.

The eval is **guided free-run**: events are teacher-forced, the holder slots and the
final value are GENERATED by the model, and we score the generated final holder +
value. This isolates "can the model run its own state" from "can it copy a trace".

Inference-time probe: at eval, allow N self-generated holder re-rolls / majority
votes to test whether extra test-time compute loosens the sparsity cliff.

Example:
    .venv-train/bin/python scripts/experiment_dense_supervision.py --seeds 0 1 2
"""
from __future__ import annotations

import argparse
import json
import os
import random
import statistics
import sys
from collections import defaultdict
from datetime import datetime, timezone

REPO = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, REPO)
os.environ.setdefault("PYTORCH_CUDA_ALLOC_CONF", "expandable_segments:True")

from factworld import tasks as TK
from factworld import train as T
from factworld.render import Renderer
from factworld.oracle import Oracle
from factworld.tokenizer import Tokenizer
from factworld.config import WorldConfig
from factworld.world import World


# A slot-bearing supervision format. We keep the natural-language event lines
# (so this is the same rendering as the benchmark) but emit per-step state as a
# single-line slot the model generates at eval:
#     role r3. <events...> holder g1 . holder g1 . ... what is a0 of the holder of r3 ? v93 .
# At eval the events are forced; the "holder <g> ." slots are generated.
HOLDER_MARK = "holder"
Q_TAIL = "what is a0 of the holder of"


def build_stream(spec, w, r, oracle, origins, L, K, rng):
    """Render a (words, hidx, vidx) stream: event lines forced, holder slots every K events."""
    ev = w.sample_hard_chain(L, episode_seed=f"K{K}|{rng.random()}")
    trace = oracle.hard_trace(ev)                       # trace[i] = agent->role assignment after i events
    role = rng.choice(w.roles)
    hist = r.render_history(tuple(ev), with_steps=True)
    words: list[str] = [f"role", role, "."]
    hidx: list[int] = []
    for i, hstr in enumerate(hist):
        words += hstr.split()
        if (i + 1) % K == 0 or i == L - 1:               # labelled checkpoint
            inv = {ro: ag for ag, ro in trace[i + 1].items()}
            words += [HOLDER_MARK, inv[role], "."]
            hidx.append(len(words) - 2)                  # index of the holder token itself
    final_holder = {ro: ag for ag, ro in trace[L].items()}[role]
    value = origins[final_holder]
    words += ["what", "is", "a0", "of", "the", "holder", "of", role, "?"]
    vidx = len(words)
    words += [value, "."]
    return words, hidx, vidx, role, final_holder, value


def make_world(spec):
    wc = WorldConfig(seed=spec.seed, n_entities=8, n_attributes=2,
                     value_vocab_size=spec.value_vocab_size, n_objects=spec.n_objects,
                     n_locations=6, k=spec.k)
    w = World(wc)
    r = Renderer()
    return w, r, Oracle(w)


def run_K(spec, K, seed, *, steps, batch, d_model, n_layers, train_n, device):
    """Train one (K, seed) cell; return (model, tok, w, r, oracle, origins)."""
    w, r, oracle = make_world(spec)
    origins = TK._fixed_origins(spec, w)
    rng = random.Random(seed)
    train = []
    for j in range(train_n):
        L = [4, 8, 16][j % 3]
        words, *_ = build_stream(spec, w, r, oracle, origins, L, K, random.Random(seed * 1000 + j))
        train.append(" ".join(words))
    tok = Tokenizer.build([w], r)
    docs = [tok.encode(t, add_eos=True) for t in train]
    docs.sort(key=len)
    d_ff = 4 * d_model
    run = T.run("gdp_hybrid", tok, docs, [], steps=steps, batch=batch, d_model=d_model,
                n_layers=n_layers, d_ff=d_ff, seed=seed, return_model=True, device=device)
    return run["model"], tok, w, r, oracle, origins


def build_eval(spec, w, r, oracle, origins, L, K, n, base_seed=300):
    out = []
    for j in range(n):
        words, hidx, vidx, role, fh, val = build_stream(spec, w, r, oracle, origins, L, K, random.Random(base_seed + L + j))
        out.append((words, hidx, vidx, role, fh, val))
    return out


def e2e_eval(model, tok, w, exs, device="cuda", max_slot=4):
    """Guided free-run: events forced, holder/value slots generated. Score final holder + value."""
    import torch
    ag_ids = {tok.token_to_id[g] for g in w.agents}
    val_ids = {tok.token_to_id[v] for v in w.value_vocab}
    dot = tok.token_to_id["."]
    model.eval()
    fh_ok = fv_ok = 0
    with torch.no_grad():
        for words, hidx, vidx, role, true_fh, true_val in exs:
            slots = set(hidx) | {vidx}
            ids, i, last_holder = [], 0, None
            while i < len(words):
                if i in slots:
                    gen = None
                    for _ in range(max_slot):
                        with torch.autocast(device, dtype=torch.bfloat16):
                            nx = int(model(torch.tensor([ids], device=device))[0, -1].float().argmax())
                        ids.append(nx)
                        target_set = ag_ids if i in hidx else val_ids
                        if gen is None and nx in target_set:
                            gen = nx
                        if nx == dot:
                            break
                    if i in hidx:
                        last_holder = gen
                    else:
                        fv_ok += int(gen == tok.token_to_id[true_val])
                    i += 1
                    while i < len(words) and words[i] != ".":
                        i += 1
                    i += 1
                else:
                    ids += tok.encode(words[i]); i += 1
            fh_ok += int(last_holder == tok.token_to_id[true_fh])
    n = len(exs)
    return fh_ok / n, fv_ok / n


def e2e_eval_majority(model, tok, w, exs, device="cuda", max_slot=4, votes=5, temp=0.7):
    """Inference-time probe: sample `votes` holder/value slots (temperature>0), majority-vote the
    final holder and value. Tests whether extra test-time compute loosens the sparsity cliff.
    Holder is the most-common generated agent across votes; value is the value paired with it."""
    import torch
    ag_list = list(w.agents)
    ag_ids = {tok.token_to_id[g] for g in ag_list}
    val_ids = {tok.token_to_id[v] for v in w.value_vocab}
    dot = tok.token_to_id["."]
    model.eval()
    fh_ok = fv_ok = 0
    gen = torch.Generator(device=device).manual_seed(0)
    with torch.no_grad():
        for words, hidx, vidx, role, true_fh, true_val in exs:
            slots = set(hidx) | {vidx}
            holder_votes = defaultdict(int)
            val_for_holder = {}
            for _ in range(votes):
                ids, i, last_holder = [], 0, None
                while i < len(words):
                    if i in slots:
                        slot_val = None
                        for _step in range(max_slot):
                            with torch.autocast(device, dtype=torch.bfloat16):
                                logits = model(torch.tensor([ids], device=device))[0, -1].float()
                                probs = torch.softmax(logits / temp, dim=-1)
                                nx = int(torch.multinomial(probs, 1, generator=gen).item())
                            ids.append(nx)
                            ts = ag_ids if i in hidx else val_ids
                            if slot_val is None and nx in ts:
                                slot_val = nx
                            if nx == dot:
                                break
                        if i in hidx:
                            last_holder = slot_val
                        else:
                            val_for_holder[last_holder] = slot_val
                        i += 1
                        while i < len(words) and words[i] != ".":
                            i += 1
                        i += 1
                    else:
                        ids += tok.encode(words[i]); i += 1
                if last_holder is not None:
                    holder_votes[last_holder] += 1
            if holder_votes:
                pred_fh = max(holder_votes, key=holder_votes.get)
                fh_ok += int(pred_fh == tok.token_to_id[true_fh])
                pred_val = val_for_holder.get(pred_fh)
                fv_ok += int(pred_val == tok.token_to_id[true_val])
    n = len(exs)
    return fh_ok / n, fv_ok / n


def main():
    ap = argparse.ArgumentParser(description="Dense-vs-sparse state-supervision sweep.")
    ap.add_argument("--seeds", type=int, nargs="+", default=[0, 1, 2])
    ap.add_argument("--Ks", type=int, nargs="+", default=[1, 2, 4, 8],
                    help="Supervision strides (K=1 dense; larger=sparser).")
    ap.add_argument("--steps", type=int, default=4000)
    ap.add_argument("--d_model", type=int, default=256)
    ap.add_argument("--n_layers", type=int, default=4)
    ap.add_argument("--batch", type=int, default=32)
    ap.add_argument("--train_n", type=int, default=8000)
    ap.add_argument("--eval_n", type=int, default=100)
    ap.add_argument("--eval_lengths", type=int, nargs="+", default=[16, 64])
    ap.add_argument("--majority_votes", type=int, default=5,
                    help="Inference-time probe: majority-vote count (0 = off).")
    ap.add_argument("--device", default="cuda")
    ap.add_argument("--out_prefix", default=None)
    a = ap.parse_args()

    spec = TK.CANONICAL["composite_copy_scale_v1"].scaled(k=5)   # k=5 non-abelian composite
    ts = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
    from pathlib import Path
    prefix = Path(a.out_prefix or f"results/dense_sweep_{ts}")
    prefix.parent.mkdir(parents=True, exist_ok=True)
    jsonl = Path(f"{prefix}.jsonl"); md = Path(f"{prefix}.md")

    print(f"=== dense-supervision sweep -> {jsonl} ===", flush=True)
    print(f"    K={a.Ks} seeds={a.seeds} L={a.eval_lengths} (gdp_hybrid d{a.d_model})", flush=True)
    agg = defaultdict(lambda: defaultdict(list))  # K -> L -> [e2e_v,...]
    for K in a.Ks:
        for seed in a.seeds:
            print(f"\n--- K={K} seed={seed} ---", flush=True)
            model, tok, w, r, oracle, origins = run_K(
                spec, K, seed, steps=a.steps, batch=a.batch, d_model=a.d_model,
                n_layers=a.n_layers, train_n=a.train_n, device=a.device)
            row = {"K": K, "seed": seed}
            for L in a.eval_lengths:
                exs = build_eval(spec, w, r, oracle, origins, L, K, a.eval_n)
                fh, fv = e2e_eval(model, tok, w, exs, device=a.device)
                row[f"L{L}_holder"] = fh; row[f"L{L}_value"] = fv
                agg[K][L].append(fv)
                line = f"    L{L}: holder={fh:.2f} value={fv:.2f}"
                if a.majority_votes > 0 and L in (a.eval_lengths[0],):
                    mfh, mfv = e2e_eval_majority(model, tok, w, exs, device=a.device, votes=a.majority_votes)
                    row[f"L{L}_maj_value"] = mfv
                    line += f"  | majority(votes={a.majority_votes}): value={mfv:.2f}"
                print(line, flush=True)
            with jsonl.open("a") as f:
                f.write(json.dumps(row) + "\n")
            del model
            import torch; torch.cuda.empty_cache()

    # markdown summary
    lines = ["# Dense-vs-sparse state-supervision sweep (s5 / non-abelian composite)",
             "", f"`scripts/experiment_dense_supervision.py`. gdp_hybrid d{a.d_model}x{a.n_layers}, "
             f"{a.steps} steps, seeds {a.seeds}. K = holder supervised every K events (K=1 dense). "
             f"Guided free-run eval (events forced, holder/value slots generated). Floor = 0.20.", "",
             "| K (stride) | " + " | ".join(f"value @L{L}" for L in a.eval_lengths) +
             " | conv(>0.5) @L" + str(a.eval_lengths[0]) + " |",
             "|---|" + "---|" * (len(a.eval_lengths) + 1)]
    for K in a.Ks:
        cells = []
        conv = 0
        for L in a.eval_lengths:
            xs = agg[K][L]
            if xs:
                cells.append(f"{statistics.mean(xs):.2f}±{statistics.pstdev(xs):.2f}")
                if L == a.eval_lengths[0]:
                    conv = sum(1 for x in xs if x > 0.5)
            else:
                cells.append("-")
        cells.append(f"{conv}/{len(a.seeds)}")
        lines.append(f"| {K} | " + " | ".join(cells) + " |")
    lines += ["", "_value = end-to-end free-running composite accuracy (generated holder + value). "
              "K=1 dense is the reproduction target (~0.95-1.00); the cliff as K grows is the sparsity result._"]
    md.write_text("\n".join(lines))
    print(f"\n=== wrote {md} ===", flush=True)


if __name__ == "__main__":
    main()
